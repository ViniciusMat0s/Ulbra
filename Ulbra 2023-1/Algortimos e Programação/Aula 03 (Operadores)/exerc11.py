#11. Faça um algoritmo que leia 3 números inteiros e:
#●       Escreve o produto (multiplicação) desses números;
#●       Escreva a soma destes números;
#●       Escreve a subtração destes números;
#●       Escreve a soma de todos os resultados acima.
numero1 = int(input("Digite um número inteiro: "))
numero2 = int(input("Digite outro número inteiro: "))
numero3 = int(input("Digite mais um número inteiro: "))
soma = numero1 + numero2 + numero3
subtração = numero1 - numero2 - numero3
total = soma + subtração
print("A soma é:",soma,"a subtração é:",subtração,"e a soma de todos os resultados é:",total)
