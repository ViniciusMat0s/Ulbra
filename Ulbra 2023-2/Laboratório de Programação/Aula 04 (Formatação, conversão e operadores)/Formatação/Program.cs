int idade  = 31;
string nome = "Lucas";

// Concatenação utilizando o operador "+".
Console.WriteLine(nome + " tem " + idade + " anos.");

// Interpolação utilizando as chaves ("{}").
Console.WriteLine($"{nome} tem {idade} anos.");

// Utilização de Placeholders.
Console.WriteLine("{0} tem {1} anos.", nome, idade);

// Usando sequência de escapes.
Console.WriteLine();

string local = "C:\\Usuarios\\Vinicitto";
Console.WriteLine(local);

string frase = "Ela falou: \"não fui eu!\"";
Console.WriteLine(frase);

string pizza = "\nPizza\n de \n mussarela";
Console.WriteLine(pizza);